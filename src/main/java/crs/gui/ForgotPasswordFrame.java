        jButton1.setBackground(new java.awt.Color(18, 45, 110));
        jButton1.setForeground(new java.awt.Color(255, 255, 255));
        jButton1.setText("Send OTP");
        jButton1.addActionListener(evt -> {
            String user = resetUsername.getText().trim();
            String email = resetEmail.getText().trim();

            if (user.isEmpty() || email.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter username and email.");
                return;
            }

            try {
                UserService us = new UserService();
                if (!us.userExists(user)) {
                    JOptionPane.showMessageDialog(this, "Username not found.");
                    return;
                }

                String storedEmail = us.getUserEmail(user);
                if (storedEmail == null || !storedEmail.equalsIgnoreCase(email)) {
                    JOptionPane.showMessageDialog(this,
                            "Email does not match our records.",
                            "Verification Failed",
                            JOptionPane.ERROR_MESSAGE);
                    return;
                }

                // Send OTP
                jButton1.setEnabled(false);
                jButton1.setText("Sending...");

                new Thread(() -> {
                    try {
                        int rnd = 100000 + new java.util.Random().nextInt(900000);
                        generatedOtp = String.valueOf(rnd);
                        verifiedEmail = email;
                        verifiedUsername = user;

                        EmailService.sendOtp(email, generatedOtp);

                        SwingUtilities.invokeLater(() -> {
                            JOptionPane.showMessageDialog(this, "OTP sent to " + email);
                            ResetOtp.setEnabled(true);
                            VerifyReset.setEnabled(true);
                            resetUsername.setEnabled(false);
                            resetEmail.setEnabled(false);
                            jButton1.setText("Sent");
                        });
                    } catch (Exception e) {
                        SwingUtilities.invokeLater(() -> {
                            JOptionPane.showMessageDialog(this, "Error sending email: " + e.getMessage());
                            jButton1.setEnabled(true);
                            jButton1.setText("Send OTP");
                        });
                    }
                }).start();

            } catch (Exception e) {
                e.printStackTrace();
            }
        });

