        ResetPasswordBtn.addActionListener(evt -> {
            String newPass = new String(ResetNewPassword.getPassword());
            if (newPass.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Enter a new password.");
                return;
            }

            try {
                UserService us = new UserService();
                if (us.updatePassword(verifiedUsername, newPass)) {
                    // Send confirmation email
                    new Thread(() -> {
                        EmailService.sendPasswordReset(verifiedEmail, newPass);
                    }).start();

                    JOptionPane.showMessageDialog(this, "Password updated successfully!\nPlease login.");
                    new LoginFrame().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to update password.");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error updating password: " + e.getMessage());
            }
        });

