        private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnDeleteActionPerformed
                int row = tblPlans.getSelectedRow();

                if (row == -1) {
                        JOptionPane.showMessageDialog(this,
                                        "Select a row to delete.");
                        return;
                }

                String planId = model.getValueAt(row, 0).toString();
                RecoveryPlan.deletePlan(planId);

                refreshTable();
                JOptionPane.showMessageDialog(this,
                                "Entry deleted.");
        }// GEN-LAST:event_btnDeleteActionPerformed

        private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
                int row = tblPlans.getSelectedRow();

                if (row == -1) {
                        JOptionPane.showMessageDialog(this,
                                        "Please select a recovery plan first.");
                        return;
                }

                String planId = model.getValueAt(row, 0).toString();

                MilestoneFrame milestoneGUI = new MilestoneFrame(planId);
                milestoneGUI.setVisible(true);
        }// GEN-LAST:event_jButton1ActionPerformed

