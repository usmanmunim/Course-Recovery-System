        private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnSearchActionPerformed
                String target = txtStudentID.getText().trim();
                if (target.isEmpty()) {
                        JOptionPane.showMessageDialog(this,
                                        "Enter Student ID to search.");
                        return;
                }
                findInTable(target);
        }// GEN-LAST:event_btnSearchActionPerformed

        private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnAddActionPerformed
                String studentID = txtStudentID.getText().trim();
                String courseID = txtCourseID.getText().trim();
                String attempt = txtAttemptNo.getText().trim();
                String status = txtStatus.getText().trim();

                if (studentID.isEmpty() || courseID.isEmpty()
                                || attempt.isEmpty() || status.isEmpty()) {
                        JOptionPane.showMessageDialog(this,
                                        "All fields required.");
                        return;
                }

                if (isDuplicate(studentID, courseID)) {
                        JOptionPane.showMessageDialog(this,
                                        "Duplicate entry exists.");
                        return;
                }

                String planID = RecoveryPlan.generatePlanID();

                RecoveryPlan newPlan = new RecoveryPlan(
                                planID, studentID, courseID,
                                Integer.parseInt(attempt), status,
                                "", ""); // Empty recommendation and grade

                RecoveryPlan.addPlan(newPlan);
                refreshTable();

                crs.services.EmailService.sendRecoveryPlanAlert(studentID + "@crs.edu", newPlan.toString());

                JOptionPane.showMessageDialog(this,
                                "Plan added successfully!");
        }// GEN-LAST:event_btnAddActionPerformed

