        private void refreshTable() {
                model.setRowCount(0);
                for (RecoveryPlan p : RecoveryPlan.loadPlans()) {
                        model.addRow(new Object[] {
                                        p.planId, p.studentId, p.courseId, p.attemptNo, p.status
                        });
                }
        }

        private boolean isDuplicate(String studentID, String courseID) {
                for (int i = 0; i < model.getRowCount(); i++) {
                        if (model.getValueAt(i, 1).equals(studentID) &&
                                        model.getValueAt(i, 2).equals(courseID)) {
                                return true;
                        }
                }
                return false;
        }

        private void findInTable(String studentId) {
                for (int i = 0; i < model.getRowCount(); i++) {
                        String sid = model.getValueAt(i, 1).toString();
                        if (sid.equalsIgnoreCase(studentId)) {
                                tblPlans.setRowSelectionInterval(i, i);
                                tblPlans.scrollRectToVisible(
                                                tblPlans.getCellRect(i, 0, true));
                                return;
                        }
                }
                JOptionPane.showMessageDialog(this,
                                "No records found for: " + studentId);
        }

