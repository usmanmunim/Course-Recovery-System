        private void ReportsBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_ReportsBtnActionPerformed
                try {
                        // Use current user if available, otherwise create default admin
                        User userToPass = currentUser;
                        if (userToPass == null) {
                                userToPass = new User("admin", "COURSE_ADMINISTRATOR", true, "admin@example.com");
                        }

                        ReportFrame frame = new ReportFrame(userToPass);
                        frame.setLocationRelativeTo(null);
                        frame.setVisible(true);
                        this.dispose();
                } catch (Throwable ex) {
                        ex.printStackTrace();
                        JOptionPane.showMessageDialog(this, "Error opening Reports: " + ex.getMessage());
                }
        }// GEN-LAST:event_ReportsBtnActionPerformed

