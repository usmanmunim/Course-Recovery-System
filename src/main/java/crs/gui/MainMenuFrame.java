        private void UserManagementBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_UserManagementBtnActionPerformed
                // Use current user if available, otherwise create default admin
                User userToPass = currentUser;
                if (userToPass == null) {
                        userToPass = new User("admin", "COURSE_ADMINISTRATOR", true, "admin@example.com");
                }

                String role = (userToPass.getRole() == null) ? ""
                                : userToPass.getRole().trim().toUpperCase().replace(" ", "_");

                if (!role.equals("COURSE_ADMINISTRATOR")) {
                        JOptionPane.showMessageDialog(this,
                                        "Access denied.\nAdmins only.",
                                        "Permission Denied",
                                        JOptionPane.ERROR_MESSAGE);
                        return;
                }

                UserManagementFrame frame = new UserManagementFrame(userToPass);
                frame.setLocationRelativeTo(null);
                frame.setVisible(true);
                this.dispose();
        }// GEN-LAST:event_UserManagementBtnActionPerformed

