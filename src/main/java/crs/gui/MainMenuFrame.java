        private void applyRolePermissions() {
                if (currentUser == null)
                        return;

                String role = currentUser.getRole();
                role = (role == null) ? "" : role.trim().toUpperCase().replace(" ", "_");

                boolean isCourseAdmin = role.equals("COURSE_ADMINISTRATOR");
                boolean isAcademicOfficer = role.equals("ACADEMIC_OFFICER");

                if (isCourseAdmin) {
                        UserManagementBtn.setEnabled(true);
                        StudentManagementBtn.setEnabled(true);
                        ReportsBtn.setEnabled(true);
                        EligibilityCheckBtn.setEnabled(false);
                        CourseRecoveryPlanBtn.setEnabled(false);
                } else if (isAcademicOfficer) {
                        UserManagementBtn.setEnabled(false);
                        StudentManagementBtn.setEnabled(true);
                        ReportsBtn.setEnabled(true);
                        EligibilityCheckBtn.setEnabled(true);
                        CourseRecoveryPlanBtn.setEnabled(true);
                } else {
                        UserManagementBtn.setEnabled(false);
                        StudentManagementBtn.setEnabled(false);
                        ReportsBtn.setEnabled(false);
                        EligibilityCheckBtn.setEnabled(false);
                        CourseRecoveryPlanBtn.setEnabled(false);
                }
        }

