    private void btnSendOtpActionPerformed(java.awt.event.ActionEvent evt) {
        String email = txtEmail.getText().trim();
        if (email.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter email.");
            return;
        }

        btnSendOtp.setEnabled(false);
        btnSendOtp.setText("Sending...");

        new Thread(() -> {
            try {
                int rnd = 100000 + new java.util.Random().nextInt(900000);
                generatedOtp = String.valueOf(rnd);
                verifiedEmail = email;

                crs.services.EmailService.sendOtp(email, generatedOtp);

                javax.swing.SwingUtilities.invokeLater(() -> {
                    javax.swing.JOptionPane.showMessageDialog(this, "OTP sent to " + email);
                    // Enable Step 2
                    txtOtp.setEnabled(true);
                    btnVerifyOtp.setEnabled(true);
                    txtOtp.requestFocus();

                    btnSendOtp.setText("Resend OTP");
                    btnSendOtp.setEnabled(true);
                });
            } catch (Exception e) {
                javax.swing.SwingUtilities.invokeLater(() -> {
                    javax.swing.JOptionPane.showMessageDialog(this, "Error sending email: " + e.getMessage());
                    btnSendOtp.setEnabled(true);
                    btnSendOtp.setText("Send OTP");
                });
            }
        }).start();
    }

    private void btnVerifyOtpActionPerformed(java.awt.event.ActionEvent evt) {
        String input = txtOtp.getText().trim();
        if (input.equals(generatedOtp)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Email Verified!");

            // Enable Step 3
            txtEmail.setEnabled(false); // Lock verified email
            btnSendOtp.setEnabled(false);
            txtOtp.setEnabled(false);
            btnVerifyOtp.setEnabled(false);

            txtUser.setEnabled(true);
            jPasswordField1.setEnabled(true);
            cmbRole.setEnabled(true);
            btnRegister.setEnabled(true);
            txtUser.requestFocus();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid OTP.", "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

