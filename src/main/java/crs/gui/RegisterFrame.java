    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {
        String user = txtUser.getText().trim();
        String pass = new String(jPasswordField1.getPassword()); // Note: treating JTextField as password for now
        String role = (String) cmbRole.getSelectedItem();

        if (user.isEmpty() || pass.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please fill all fields.");
            return;
        }

        crs.services.UserService us = new crs.services.UserService();
        try {
            if (us.isUsernameTaken(user)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Username '" + user + "' is already taken.");
                return;
            }

            us.registerUser(user, pass, role.toUpperCase().replace(" ", "_"), verifiedEmail);

            // Send welcome email
            new Thread(() -> {
                new crs.services.EmailService().sendRegistrationConfirmation(verifiedEmail, user, role, pass);
            }).start();

            // Success
            javax.swing.JOptionPane.showMessageDialog(this, "Registration Successful!\nYou can now login.");
            new LoginFrame().setVisible(true);
            this.dispose();

        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error registering: " + e.getMessage());
        }
    }

